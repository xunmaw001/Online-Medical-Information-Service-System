      <el-col :span="12">
        <el-form-item   v-if="flag=='yisheng'"  label="医龄" prop="yiling">
          <el-input v-model="ruleForm.yiling"               placeholder="医龄" clearable></el-input>
        </el-form-item>
      </el-col>
      <el-col :span="12">
        <el-form-item   v-if="flag=='yisheng'"  label="教育背景" prop="jiaoyubeijing">
          <el-input v-model="ruleForm.jiaoyubeijing"               placeholder="教育背景" clearable></el-input>
        </el-form-item>
      </el-col>
      <el-col :span="12">
        <el-form-item   v-if="flag=='yisheng'"  label="工作经历" prop="gongzuojingli">
          <el-input v-model="ruleForm.gongzuojingli"               placeholder="工作经历" clearable></el-input>
        </el-form-item>
      </el-col>
      <el-col :span="12">
        <el-form-item   v-if="flag=='yisheng'"  label="毕业学校" prop="biyexuexiao">
          <el-input v-model="ruleForm.biyexuexiao"               placeholder="毕业学校" clearable></el-input>
        </el-form-item>
      </el-col>
      <el-form-item v-if="flag=='users'" label="用户名" prop="username">
        <el-input v-model="ruleForm.username" 
        placeholder="用户名"></el-input>
      </el-form-item>
      <el-col :span="24">
      <el-form-item>
        <el-button type="primary" @click="onUpdateHandler">修 改</el-button>
      </el-form-item>
